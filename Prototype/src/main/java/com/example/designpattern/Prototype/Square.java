package com.example.designpattern.Prototype;

public class Square extends Shapes{
	
	private int area;

	

	public int getArea() {
		return area;
	}



	public void setArea(int area) {
		this.area = area;
	}



	@Override
	public String toString() {
		return "Square [area=" + area + "]";
	}



	
	
	

}
